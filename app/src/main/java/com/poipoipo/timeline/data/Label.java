package com.poipoipo.timeline.data;

public class Label {
    private int id;
    private String value;

    public Label(){}

    public void setId(int id) {
        this.id = id;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public String getValue() {
        return value;
    }
}
